package Modelo;

public class Calculos {
	
	public float CalcAdicao(float Primeiro, float Segundo) {
		float Resultado = Primeiro + Segundo;
		return Resultado;
	}

	public float CalcSubtracao(float Primeiro, float Segundo) {
		float Resultado = Primeiro - Segundo;
		return Resultado;
	}
	
	public float CalcMultiplicacao(float Primeiro, float Segundo) {
		float Resultado = Primeiro * Segundo;
		return Resultado;
	}
	
	public float CalcDivisao(float Primeiro, float Segundo) {
		float Resultado = Primeiro / Segundo;
		return Resultado;
	}

}
